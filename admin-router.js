// LLM-роутер сообщений владельца: страховочная сеть под быстрыми шаблонами tg-bot.js.
// Если админ написал команду в свободной формулировке («Евгений с Авито купил Game Pass...»),
// шаблон её не поймает — тогда один дешёвый LLM-вызов решает, продажа это, команда каталогу
// или обычный тест клиентского бота. Без роутера такая продажа молча уходила в клиентского
// бота, тот вежливо отвечал «зафиксировал» — и запись терялась без следа.

import { chat } from './llm.js';
import { extractJson } from './crm.js';

const ROUTER_PROMPT = `Ты — маршрутизатор сообщений владельца магазина игровых подписок его служебному Telegram-боту. Определи тип сообщения и верни СТРОГО один JSON-объект без пояснений: {"intent": "sale" | "renewal" | "catalog" | "chat"}
- "sale" — заметка о продаже подписки или игры клиенту, в любой формулировке: «продал Ивану...», «Иван с Авито купил...», «оформил для Ивана...»
- "renewal" — заметка о продлении существующей подписки клиента
- "catalog" — команда каталогу магазина: скрыть/показать игру, изменить цену или скидку, витринные флаги (витрина/новинка/хит), карточка игры
- "chat" — всё остальное: вопросы о играх, подбор, тест клиентского бота
Если сомневаешься между "sale" и "chat" — выбирай "sale": владелец увидит карточку записи и сможет её отменить, а молча потерянная продажа хуже лишней карточки.`;

// Дешёвый гейт перед LLM-вызовом: похоже ли сообщение на админскую заметку/команду.
// Команды («/start», «/clients») сюда не попадают — они обрабатываются раньше.
// Русские написания подписок (плюс/экстра/делюкс/пасс…) и «запис…» тоже в гейте:
// «Записал Диме плюс делюкс на год» без них молча уходил в клиентского бота
const ADMINISH = /(прода|продл|купил|оформил|взял|запис|подписк|game ?pass|ps ?plus|ea ?play|пасс|плюс|экстра|делюкс|эссеншл|ультимейт|авито|телег|скидк|цен[аыуе]|скрой|спрячь|витрин|новинк)/i;

export function looksAdminish(text) {
  if (text.startsWith('/')) return false;
  return ADMINISH.test(text);
}

// Возвращает 'sale' | 'renewal' | 'catalog' | 'chat'. При невалидном ответе LLM — 'chat'
// (ошибку сети/LLM не глотаем — пусть решает вызывающий код).
export async function classifyAdminIntent(text) {
  const res = await chat([
    { role: 'system', content: ROUTER_PROMPT },
    { role: 'user', content: text },
  ]);
  const p = extractJson(res.content);
  return ['sale', 'renewal', 'catalog', 'chat'].includes(p?.intent) ? p.intent : 'chat';
}
